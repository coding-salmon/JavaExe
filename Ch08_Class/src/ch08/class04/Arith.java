package ch08.class04;


/* 객체가 최초 생설될때 비로서 메서드가 메모리에 올라간다.
 * 
 */
public class Arith {
	public  int add (int num0 , int num1) {
		return num0 + num1;
	}
	public  int sub (int num0 , int num1) {
			return num0 - num1;
	}
	public  int mul (int num0 , int num1) {
				return num0 * num1;
	}
	public  int div(int num0, int num1) {
		return num0 / num1;
	}

}
